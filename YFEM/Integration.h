	template<typename T>
	T InnerProduct(const Polynomial<T>& u, const Polynomial<T>& v)
	{
		T sum = 0;
		for (dgree_t i = 0; i <= u.dgree; ++i)
		{
			for (dgree_t j = 0; j <= i; ++j)
			{
				for (dgree_t p = 0; p <= v.dgree; ++p)
				{
					for (dgree_t q = 0; q <= p; ++q)
					{
						if ((i + p) % 2 == 0 && (j + q) % 2 == 0)
						{
							sum += u[i*(i + 1) / 2 + j] * v[p*(p + 1) / 2 + q] * 4. / (i + p - j - q + 1) / (j + q + 1);
						}
					}
				}
			}
		}
		return sum;
	}

	

